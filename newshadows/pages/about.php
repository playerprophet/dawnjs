      <p><strong>Anna Khatri</strong> is a sheltered 17-year-old girl from a tiny farming village on the Second Continent. She runs a farm with her mother, Saanvi, who was once a famous adventurer. She always figured one day that she would leave, and Those Without Shadows: Mind is about that journey.</p>

      <p>Anna is sweet and patient, a lover of both animals and people. While she tends to be fond of everyone, she is particularly preoccupied with finding love and romance, to a fault. Her sense of anticipation for romance tends to cloud her judgment of others.</p>

